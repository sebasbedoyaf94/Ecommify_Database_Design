CREATE INDEX zip_codes_city_trgm_idx ON public.zip_codes USING gin (city gin_trgm_ops);


-- public.customers definition

-- Drop table

-- DROP TABLE public.customers;

CREATE TABLE public.customers (
	customer_id uuid DEFAULT uuid_generate_v4() NOT NULL,
	customer_unique_id uuid NOT NULL,
	customer_zip_code int8 NOT NULL,
	CONSTRAINT customers_pk PRIMARY KEY (customer_id),
	CONSTRAINT customers_zip_codes_fk FOREIGN KEY (customer_zip_code) REFERENCES public.zip_codes(zip_code_prefix)
);
CREATE INDEX customers_customer_zip_code_idx ON public.customers USING btree (customer_zip_code);


-- public.orders definition

-- Drop table

-- DROP TABLE public.orders;

CREATE TABLE public.orders (
	order_id uuid DEFAULT uuid_generate_v4() NOT NULL,
	customer_id uuid NOT NULL,
	order_status varchar NOT NULL,
	order_purchase_timestamp timestamp NOT NULL,
	order_approved_at timestamp NULL,
	order_delivered_carrier_date timestamp NULL,
	order_delivered_customer_date timestamp NULL,
	order_estimated_delivery_date timestamp NULL,
	CONSTRAINT orders_check CHECK (((order_status)::text = ANY ((ARRAY['approved'::character varying, 'canceled'::character varying, 'created'::character varying, 'delivered'::character varying, 'invoiced'::character varying, 'processing'::character varying, 'shipped'::character varying, 'unavailable'::character varying])::text[]))),
	CONSTRAINT orders_pk PRIMARY KEY (order_id),
	CONSTRAINT orders_customers_fk FOREIGN KEY (customer_id) REFERENCES public.customers(customer_id)
);
CREATE INDEX orders_customer_id_idx ON public.orders USING btree (customer_id);
CREATE INDEX orders_order_status_idx ON public.orders USING btree (order_status);


-- public.sellers definition

